

///Loops/iteration statement



void main(){

  ///for loop

  for(int i=10; i>=1;--i){
    print(i);

  }
  print("=====================================");
  ///while loop
  int x=11;
  while(x>=1){
    x--;
    print(x);
  }
  print("======================================");

  ///do-while
  int a =10;
  do{
    print(a);
    a--;
  }
  while(a>=1);
}